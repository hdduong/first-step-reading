// Scans public/audio/ for .mp3 clips and regenerates src/lib/clips-manifest.js
// so the app knows which recorded clips exist. Run after adding/removing clips:
//
//   npm run audio:manifest
//
import { readdirSync, statSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const root = fileURLToPath(new URL("..", import.meta.url));
const audioDir = join(root, "public", "audio");
const manifestFile = join(root, "src", "lib", "clips-manifest.js");

const keys = [];
const walk = (dir) => {
  let entries;
  try {
    entries = readdirSync(dir);
  } catch {
    return; // public/audio doesn't exist yet
  }
  for (const entry of entries) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) walk(full);
    else if (entry.toLowerCase().endsWith(".mp3"))
      keys.push(relative(audioDir, full).replace(/\\/g, "/").replace(/\.mp3$/i, ""));
  }
};
walk(audioDir);
keys.sort();

const body = keys.length
  ? `\n  ${keys.map((k) => JSON.stringify(k)).join(",\n  ")},\n`
  : "";
const out = `// Auto-generated by scripts/build-audio-manifest.mjs — do not edit by hand.
// Lists the recorded clips present in public/audio/ (keys, no extension).
// Run \`npm run audio:manifest\` after adding or removing clip files.
export const CLIPS = new Set([${body}]);
`;
writeFileSync(manifestFile, out);
console.log(`Wrote ${keys.length} clip(s) to src/lib/clips-manifest.js`);
