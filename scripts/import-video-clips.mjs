import {
  copyFileSync,
  existsSync,
  mkdirSync,
  readFileSync,
  writeFileSync,
} from "node:fs";
import { basename, dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { clipList } from "./clip-list.mjs";

const root = fileURLToPath(new URL("..", import.meta.url));
const defaultManifest = join(root, "..", "clips", "word-level-v1", "manifest.csv");
const outDir = join(root, "public", "video", "disk-clips");
const outManifest = join(root, "src", "lib", "video-clips-manifest.js");

const args = process.argv.slice(2);
const argValue = (flag) => {
  const i = args.indexOf(flag);
  return i === -1 ? null : args[i + 1] || null;
};

const sourceManifest = argValue("--manifest") || defaultManifest;
if (!existsSync(sourceManifest))
  throw new Error(`Video clip manifest not found: ${sourceManifest}`);

const slug = (value) =>
  String(value || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "");

const parseCsv = (text) => {
  const rows = [];
  let row = [];
  let field = "";
  let quoted = false;
  for (let i = 0; i < text.length; i++) {
    const ch = text[i];
    const next = text[i + 1];
    if (quoted) {
      if (ch === '"' && next === '"') {
        field += '"';
        i++;
      } else if (ch === '"') {
        quoted = false;
      } else {
        field += ch;
      }
    } else if (ch === '"') {
      quoted = true;
    } else if (ch === ",") {
      row.push(field);
      field = "";
    } else if (ch === "\n") {
      row.push(field.replace(/\r$/, ""));
      rows.push(row);
      row = [];
      field = "";
    } else {
      field += ch;
    }
  }
  if (field || row.length) {
    row.push(field.replace(/\r$/, ""));
    rows.push(row);
  }
  const [headers, ...body] = rows;
  return body
    .filter((cells) => cells.length === headers.length)
    .map((cells) =>
      Object.fromEntries(headers.map((header, i) => [header, cells[i]])),
    );
};

const allowedWordSlugs = new Set(
  [...clipList().keys()]
    .filter((key) => key.startsWith("words/"))
    .map((key) => key.slice("words/".length)),
);

const byWord = {};
let copied = 0;
for (const row of parseCsv(readFileSync(sourceManifest, "utf8"))) {
  const key = slug(row.word);
  if (!allowedWordSlugs.has(key)) continue;
  const source = row.clip_path;
  if (!source || !existsSync(source)) continue;
  const disk = row.disk || "Disk";
  const relativePath = `${disk}/${basename(source)}`;
  const destination = join(outDir, disk, basename(source));
  mkdirSync(dirname(destination), { recursive: true });
  copyFileSync(source, destination);
  copied++;
  byWord[key] ||= [];
  byWord[key].push({
    word: row.word,
    disk,
    path: relativePath.replace(/\\/g, "/"),
    approxTime: row.approx_time,
  });
}

const ordered = Object.fromEntries(
  Object.entries(byWord)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([key, clips]) => [
      key,
      clips.sort(
        (a, b) => a.disk.localeCompare(b.disk) || a.path.localeCompare(b.path),
      ),
    ]),
);

const body = JSON.stringify(ordered, null, 2).replace(/\n/g, "\n");
writeFileSync(
  outManifest,
  `// Auto-generated by scripts/import-video-clips.mjs - do not edit by hand.\n` +
    `// Maps normalized Book 1 words to imported Disk 1/2 video clips.\n` +
    `export const VIDEO_CLIPS = ${body};\n`,
);

console.log(
  `Copied ${copied} video clip(s) for ${Object.keys(ordered).length} word(s).`,
);
console.log(`Wrote ${outManifest}`);
